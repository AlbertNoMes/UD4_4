
public class InstruccionesVariablesAPP {

	public static void main(String[] args) {
		
		int varN=10;
		
		System.out.println("La variable N es "+varN);
		varN+=77;
		System.out.println("N + 77 = "+varN);
		varN-=3;
		System.out.println("N - 3 = "+varN);
		System.out.println("El doble de N es = "+(varN*2));

	}

}
